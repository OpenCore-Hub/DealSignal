// PageInfo holds extracted data for a single PDF page.
type PageInfo struct {
	Number int
	Width  int
	Height int
	Text   string
}

// Chunk holds a text segment extracted from a page.
type Chunk struct {
	Text string
	Bbox []byte
}

// pageBbox returns a JSON bounding box for the whole page.
func pageBbox(width, height int) []byte {
	return []byte(fmt.Sprintf(`{"x":0,"y":0,"w":%d,"h":%d}`, width, height))
}

// ExtractPages extracts page count, dimensions and text from a PDF file.
func ExtractPages(ctx context.Context, filePath string) ([]PageInfo, error) {
	f, r, err := pdf.Open(filePath)
	if err != nil {
		return nil, fmt.Errorf("open pdf: %w", err)
	}
	defer f.Close()

	total := r.NumPage()
	pages := make([]PageInfo, 0, total)
	for i := 1; i <= total; i++ {
		page := r.Page(i)
		if page.V.IsNull() {
			continue
		}

		w, h := pageDimensions(page)
		text, err := page.GetPlainText(nil)
		if err != nil {
			text = ""
		}

		pages = append(pages, PageInfo{
			Number: i,
			Width:  w,
			Height: h,
			Text:   text,
		})
	}
	return pages, nil
}

func pageDimensions(p pdf.Page) (int, int) {
	box := p.V.Key("MediaBox")
	if box.IsNull() {
		box = p.V.Key("CropBox")
	}
	if box.IsNull() {
		return 612, 792
	}
	if box.Len() < 4 {
		return 612, 792
	}
	w := box.Index(2).Float64() - box.Index(0).Float64()
	h := box.Index(3).Float64() - box.Index(1).Float64()
	if w <= 0 || h <= 0 {
		return 612, 792
	}

	const maxEdge = 2048
	scale := 1.0
	if w > h && w > maxEdge {
		scale = maxEdge / w
	} else if h > maxEdge {
		scale = maxEdge / h
	}
	return int(w * scale), int(h * scale)
}

// renderPage creates a placeholder PNG image for a page.
func renderPage(p PageInfo) ([]byte, error) {
	img := image.NewRGBA(image.Rect(0, 0, p.Width, p.Height))
	draw.Draw(img, img.Bounds(), &image.Uniform{color.White}, image.Point{}, draw.Src)

	label := "Page " + strconv.Itoa(p.Number)
	drawLabel(img, label)

	var buf bytes.Buffer
	if err := png.Encode(&buf, img); err != nil {
		return nil, fmt.Errorf("encode png: %w", err)
	}
	return buf.Bytes(), nil
}

func drawLabel(img *image.RGBA, label string) {
	startX, startY := 20, 40
	x := startX
	for _, ch := range label {
		glyph := font5x7(ch)
		for row, bits := range glyph {
			for col := 0; col < 5; col++ {
				if bits&(1<<(4-col)) != 0 {
					img.Set(x+col, startY+row, color.Black)
				}
			}
		}
		x += 6
	}
}

func font5x7(r rune) [7]byte {
	switch {
	case r >= '0' && r <= '9':
		return digitFont[r-'0']
	case r == ' ':
		return [7]byte{0, 0, 0, 0, 0, 0, 0}
	case r >= 'A' && r <= 'Z':
		return letterFont[r-'A']
	case r >= 'a' && r <= 'z':
		return letterFont[r-'a']
	}
	return [7]byte{0, 0, 0, 0, 0, 0, 0}
}

